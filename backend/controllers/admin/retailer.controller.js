// ====== BULK REGISTER RETAILERS ======
export const bulkRegisterRetailers = async (req, res) => {
    try {
        /* ---------------- ADMIN CHECK ---------------- */
        if (!req.user || req.user.role !== "admin") {
            return res.status(403).json({
                success: false,
                message: "Only admins can upload retailers",
            });
        }

        /* ---------------- FILE CHECK ---------------- */
        if (!req.file) {
            return res.status(400).json({
                success: false,
                message: "Excel/CSV file is required",
            });
        }

        /* ---------------- READ EXCEL ---------------- */
        const workbook = XLSX.read(req.file.buffer, { type: "buffer" });
        const sheet = workbook.Sheets[workbook.SheetNames[0]];

        const rows = XLSX.utils.sheet_to_json(sheet, {
            raw: false,
            defval: "",
        });

        const retailersToInsert = [];
        const failedRows = [];

        /* ---------------- PROCESS EACH ROW ---------------- */
        for (let i = 0; i < rows.length; i++) {
            const row = rows[i];

            /* =================================================
               âœ… EXCEL-PROOF NUMERIC SANITIZATION
            ================================================= */
            const contactNo = row.contactNo
                ? row.contactNo
                    .toString()
                    .split(".")[0]
                    .replace(/\D/g, "")
                    .slice(0, 10)
                : "";

            const shopPincode = row.shopPincode
                ? row.shopPincode
                    .toString()
                    .split(".")[0]
                    .replace(/\D/g, "")
                    .slice(0, 6)
                : "";

            const accountNumber = row.accountNumber
                ? row.accountNumber
                    .toString()
                    .split(".")[0]
                    .replace(/\D/g, "")
                : "";

            /* ---------------- EXTRACT FIELDS ---------------- */
            const {
                shopName,
                shopAddress,
                shopCity,
                shopState,
                businessType,
                ownershipType,
                GSTNo,
                name,
                PANCard,
                email,
                govtIdType,
                govtIdNumber,
                bankName,
                IFSC,
                branchName,
            } = row;

            /* ---------------- VALIDATION ---------------- */
            const missingFields = [];

            // Shop
            if (!shopName) missingFields.push("shopName");
            if (!shopAddress) missingFields.push("shopAddress");
            if (!shopCity) missingFields.push("shopCity");
            if (!shopState) missingFields.push("shopState");
            if (!shopPincode) missingFields.push("shopPincode");
            if (!businessType) missingFields.push("businessType");
            if (!PANCard) missingFields.push("PANCard");

            // Retailer
            if (!name) missingFields.push("name");
            if (!contactNo) missingFields.push("contactNo");

            // Bank
            if (!bankName) missingFields.push("bankName");
            if (!accountNumber) missingFields.push("accountNumber");
            if (!IFSC) missingFields.push("IFSC");
            if (!branchName) missingFields.push("branchName");

            if (missingFields.length > 0) {
                failedRows.push({
                    rowNumber: i + 2,
                    reason: `Missing required fields: ${missingFields.join(", ")}`,
                    data: row,
                });
                continue;
            }

            /* ---------------- CONTACT VALIDATION ---------------- */
            if (!/^[6-9]\d{9}$/.test(contactNo)) {
                failedRows.push({
                    rowNumber: i + 2,
                    reason: `Invalid contact number: ${contactNo}`,
                    data: row,
                });
                continue;
            }

            /* ---------------- PINCODE VALIDATION ---------------- */
            if (shopPincode.length !== 6) {
                failedRows.push({
                    rowNumber: i + 2,
                    reason: `Invalid pincode: ${shopPincode}`,
                    data: row,
                });
                continue;
            }

            /* ---------------- EMAIL VALIDATION ---------------- */
            if (email) {
                const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
                if (!emailRegex.test(email)) {
                    failedRows.push({
                        rowNumber: i + 2,
                        reason: `Invalid email format: ${email}`,
                        data: row,
                    });
                    continue;
                }
            }

            /* ---------------- DUPLICATE CHECK ---------------- */
            const exists = await Retailer.findOne({ contactNo });
            if (exists) {
                failedRows.push({
                    rowNumber: i + 2,
                    reason: `Duplicate contact number: ${contactNo}`,
                    data: row,
                    existingRetailer: {
                        id: exists._id,
                        name: exists.name,
                        contactNo: exists.contactNo,
                        uniqueId: exists.uniqueId,
                        retailerCode: exists.retailerCode,
                    },
                });
                continue;
            }

            /* ---------------- TEMP EMAIL ---------------- */
            const finalEmail = email
                ? email
                : `temp+${contactNo}@retailer.local`;

            /* ---------------- BUILD OBJECT ---------------- */
            retailersToInsert.push({
                name,
                contactNo,
                email: finalEmail,
                password: contactNo,

                govtIdType: govtIdType || undefined,
                govtIdNumber: govtIdNumber || undefined,

                shopDetails: {
                    shopName,
                    businessType,
                    PANCard,
                    ownershipType: ownershipType || undefined,
                    GSTNo: GSTNo || undefined,
                    shopAddress: {
                        address: shopAddress,
                        city: shopCity,
                        state: shopState,
                        pincode: shopPincode,
                    },
                },

                bankDetails: {
                    bankName,
                    accountNumber,
                    IFSC,
                    branchName,
                },

                phoneVerified: true,
                tnc: false,
                pennyCheck: false,
            });
        }

        /* ---------------- INSERT ---------------- */
        let insertedRetailers = [];
        if (retailersToInsert.length > 0) {
            try {
                insertedRetailers = await Retailer.insertMany(
                    retailersToInsert,
                    { ordered: false }
                );
            } catch (err) {
                if (err.insertedDocs) {
                    insertedRetailers = err.insertedDocs;
                }

                if (err.writeErrors) {
                    err.writeErrors.forEach((e) => {
                        const doc = retailersToInsert[e.index];
                        failedRows.push({
                            rowNumber: e.index + 2,
                            reason: e.message,
                            data: {
                                name: doc.name,
                                contactNo: doc.contactNo,
                                shopName: doc.shopDetails?.shopName,
                            },
                        });
                    });
                }
            }
        }

        /* ---------------- RESPONSE ---------------- */
        const response = {
            success: true,
            summary: {
                totalRows: rows.length,
                successful: insertedRetailers.length,
                failed: failedRows.length,
                successRate: rows.length
                    ? `${((insertedRetailers.length / rows.length) * 100).toFixed(2)}%`
                    : "0%",
            },
            insertedRetailers: insertedRetailers.map((r) => ({
                id: r._id,
                name: r.name,
                email: r.email,
                contactNo: r.contactNo,
                uniqueId: r.uniqueId,
                retailerCode: r.retailerCode,
                shopName: r.shopDetails?.shopName,
            })),
            failedRows,
        };

        if (insertedRetailers.length === 0) {
            return res.status(400).json({
                success: false,
                message: "No retailers were added",
                ...response,
            });
        }

        return res.status(failedRows.length ? 207 : 201).json({
            success: true,
            message: failedRows.length
                ? "Bulk upload completed with partial success"
                : "All retailers added successfully",
            ...response,
        });
    } catch (error) {
        console.error("âŒ Bulk retailer upload error:", error);
        return res.status(500).json({
            success: false,
            message: "Server error",
            error: error.message,
        });
    }
};

// ====== UPDATE RETAILER DATES (in campaign) ======
export const updateRetailerDates = async (req, res) => {
    try {
        const { campaignId, retailerId } = req.params;
        const { startDate, endDate } = req.body;

        // admin check
        if (!req.user || req.user.role !== "admin")
            return res
                .status(403)
                .json({ message: "Only admin can update dates" });

        const campaign = await Campaign.findById(campaignId);
        if (!campaign)
            return res.status(404).json({ message: "Campaign not found" });

        const retailerEntry = campaign.assignedRetailers.find(
            (r) => r.retailerId.toString() === retailerId.toString()
        );

        if (!retailerEntry)
            return res
                .status(404)
                .json({ message: "Retailer not assigned to this campaign" });

        // Update only if values are provided
        if (startDate) retailerEntry.startDate = new Date(startDate);
        if (endDate) retailerEntry.endDate = new Date(endDate);
        retailerEntry.updatedAt = new Date();

        await campaign.save();

        res.status(200).json({
            message: "Retailer dates updated successfully",
            retailer: retailerEntry,
        });
    } catch (err) {
        console.error("Retailer date update error:", err);
        res.status(500).json({ message: "Server error", error: err.message });
    }
};

